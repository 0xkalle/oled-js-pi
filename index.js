function startscrollright(start, stop){
  var cmdSeq = [
    OLED.RIGHT_HORIZONTAL_SCROLL,
    0X00, start,
    0X00, stop,
    0X00, 0XFF,
    OLED.ACTIVATE_SCROLL
  ];

  var i, cmdSeqLen = cmdSeq.length;

  for (i = 0; i < cmdSeqLen; i += 1) {
    writeI2C('cmd', cmdSeq[i]);
  }
}

